    private void importarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importarActionPerformed
        JFileChooser Arquivo = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Arquivos pdf e docx","pdf", "docx");
        Arquivo.addChoosableFileFilter(filtro);
        Arquivo.setAcceptAllFileFilterUsed(false);
        int opc = Arquivo.showOpenDialog(this);
        
        if(opc==JFileChooser.APPROVE_OPTION){
        String nomeArqivo = Arquivo.getSelectedFile().getName();
        String caminhoArquivo = Arquivo.getSelectedFile().toString();
        
        campoDocumento.setText(nomeArqivo);
        campoCaminho.setText(caminhoArquivo);
        
        }
    }//GEN-LAST:event_importarActionPerformed

