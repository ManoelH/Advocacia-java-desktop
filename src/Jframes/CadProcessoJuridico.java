    private void CadastrarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarUActionPerformed

        conexao.ConectaBanco conecta = new ConectaBanco();
        conecta.conecta();
        
        processo.setNprocesso(campoNumeroProcesso.getText());
        processo.setAssunto((String) campoAssunto.getSelectedItem());
        processo.setCep(campoCep.getText());
        processo.setAdvogado(CampoAdvogado.getText());
        processo.setCidade(campoCidade.getText());
        processo.setClienteJ(campoClienteJuridico.getText());
        processo.setJuiz(campoJuiz.getText());
        processo.setReu(campoReu.getText());
        processo.setTestemunha(campoTestemunha.getText());
        processo.setAbertura(campoDataInicio.getText());
        
        if(inserir.inserirProcessoCJ(processo)==true){
        
        campoNumeroProcesso.setText(null);
        campoCidade.setText(null);
        campoClienteJuridico.setText(null);
        campoJuiz.setText(null);
        CampoAdvogado.setText(null);
        campoTestemunha.setText(null);
        campoReu.setText(null);
        campoCep.setText(null);
        campoDataInicio.setText(null);
        campoCaminho.setText(null);
        campoDocumento.setText(null);
        
        campoCep.setEnabled(false);
        campoNumeroProcesso.setEnabled(false);
        campoAssunto.setEnabled(false);
        campoCidade.setEnabled(false);
        campoClienteJuridico.setEnabled(false);
        campoJuiz.setEnabled(false);
        CampoAdvogado.setEnabled(false);
        campoTestemunha.setEnabled(false);
        campoReu.setEnabled(false);
        CadastrarU.setEnabled(false);
        campoDataInicio.setEnabled(false);
        importar.setEnabled(false);
        Novo.setEnabled(true);
        JOptionPane.showMessageDialog(rootPane,"Dados inseridos com sucesso");
        conecta.desconecta();
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos");
        
        
        
        
    }//GEN-LAST:event_CadastrarUActionPerformed

